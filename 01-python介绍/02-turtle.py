import turtle   # 导入 turtle模块
turtle.showturtle()   # 显示箭头
turtle.write('shuaishuailiu')  # 写字符转
turtle.forward(300)  # 前进300像素
turtle.color('red')  # 画笔颜色改为red
turtle.left(90)  # 箭头左转90度
turtle.forward(300)
turtle.goto(0, 50)  # 去坐标（0，50）
turtle.goto(0, 0)
turtle.goto(0, 50)
# turtle.penup()   # 抬笔。这样，路径就不会画出来
turtle.goto(50, 50)
turtle.pendown()  # 下笔
turtle.circle(100)  # 画圆  值为 半径
