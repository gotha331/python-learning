# 绘制奥运五环
import turtle

t = turtle.Pen()
t.width(10)
t.color('red')
t.circle(50)
t.penup()
t.goto(120, 0)
t.pendown()
t.color('yellow')
t.circle(50)
t.penup()
t.goto(240, 0)
t.pendown()
t.color('blue')
t.circle(50)
t.penup()
t.goto(60, -50)
t.pendown()
t.color('#000')
t.circle(50)
t.penup()
t.goto(180, -50)
t.pendown()
t.color('green')
t.circle(50)
