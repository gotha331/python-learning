# 填充与对齐
# ^、<、> 分别是居中、左对齐、右对齐，后面带宽度
# :后面带填充的字符，只能是一个字符，不指定的话默认为空格

e = "{:*>8}".format(256)
print(e)  # *****256

f = "我是{0},我喜欢数字{1:&^10}".format("刘帅帅", "666")
print(f)  # 我是刘帅帅,我喜欢数字&&&666&&&&
